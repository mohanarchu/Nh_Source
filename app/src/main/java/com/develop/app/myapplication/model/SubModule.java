
package com.develop.app.myapplication.model;


public class SubModule {

    private String submoduleName;
    private String subModulelistService;
    private String subModulecountService;

    public String getSubmoduleName() {
        return submoduleName;
    }

    public void setSubmoduleName(String submoduleName) {
        this.submoduleName = submoduleName;
    }

    public String getSubModulelistService() {
        return subModulelistService;
    }

    public void setSubModulelistService(String subModulelistService) {
        this.subModulelistService = subModulelistService;
    }

    public String getSubModulecountService() {
        return subModulecountService;
    }

    public void setSubModulecountService(String subModulecountService) {
        this.subModulecountService = subModulecountService;
    }

}
